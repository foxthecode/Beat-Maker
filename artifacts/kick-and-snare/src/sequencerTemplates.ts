/** Sequencer Templates — 16 + 32 step, with humanized velocities
 *  ALL tracks defined; loadTemplate activates them automatically.
 *  vel arrays: 100=accent, 85=normal, 70=offbeat, 50=ghost, 0=silent step (ignored)
 */

export interface DrumTemplate {
  id: string;
  name: string;
  genre: string;
  icon: string;
  bpm?: number;
  color: string;
  /** 16-step on/off grid */
  steps: Partial<Record<string, number[]>>;
  /** humanized velocities for 16-step (same indices as steps) */
  vel?: Partial<Record<string, number[]>>;
  /** 32-step variant — more detailed second bar */
  steps32?: Partial<Record<string, number[]>>;
  vel32?: Partial<Record<string, number[]>>;
}

// ─── velocity shorthands ──────────────────────────────────────────────────────
const A = 100; // accent
const N = 85;  // normal
const O = 70;  // offbeat
const G = 52;  // ghost
const _ = 80;  // default fill (used for silent steps; engine ignores if step=0)

// ─── 16-step "double" helper — repeats a 16-arr twice with optional bar-2 overlay
function dbl(a16: number[], b16?: number[]): number[] {
  return [...a16, ...(b16 ?? a16)];
}

// ─── velocity "double" helper
function vdbl(v16: number[], vb?: number[]): number[] {
  return [...v16, ...(vb ?? v16)];
}

